print("calculadora de area y perimetro de un rectangulo")
base = float(input("introduza los valores de la base: "))
altura = float(input("introduzca los varoles de la altura: "))
area = base * altura
perimetro = 2 * base + 2 * altura
print(f"El area del rectangulo es {area} y su perimetro es {perimetro}")