using System;

namespace tarea1{
    public class program{
        static void Main(string[] args){
            int numero = Console.ReadLine("Ingrese un numero para ver si es par o impar");
            int par = 0;
            int impar 0;

            for (int i = 0; i < 10; i++)
            {
                if (par = impar) {
                    par += 1;
                }
                else {
                    impar += 1
                }
            }

            if (par > impar){
                Console.WriteLine("el numero es impar:" + numero);
            }
            else{
                Console.WriteLine("el numero es par:" + numero);
            }
        }
    }
}